package service;

public class Calculator {
	
	public int sum(int a, int b) {
		int c = a+b;
		return c;
	}
	
	public int substract(int a, int b) {
		int c = a-b;
		return c;
	}
	
	public int multiply(int a, int b) {
		int c = a*b;
		return c;
	}
	
	public int division(int a, int b) {
		int c = a/b;
		return c;
	}

}
